    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        String nomeSecretaria = jTextField1.getText();
        
        try{
            if(nomeSecretaria.isEmpty()){
                throw new Exception("Preencha todos os campos");
            }
            Secretaria secretaria = new Secretaria(null, nomeSecretaria);
            secretariaController.create(secretaria);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this,
                    e.getMessage(),
                    "ERRO",
                    JOptionPane.ERROR_MESSAGE);

        }
        atualizarTabela();

    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        Integer idSecretaria = Integer.valueOf(jTextField2.getText());
            try{
                if(JOptionPane.showConfirmDialog(this,"Remover a secretaria de " + jTextField1.getText() + " ?") == JOptionPane.OK_OPTION){
                    secretariaController.destroy(idSecretaria);
                    JOptionPane.showConfirmDialog(this,
                            "Secretaria de " + jTextField1.getText() + " removido com sucesso",
                            "SUCESSO",
                            JOptionPane.INFORMATION_MESSAGE);
                    jButton4.setEnabled(false);            
                    jButton3.setEnabled(true);
                    jTextField1.setText("");
                    jTextField2.setText("");
                }                
            }catch (Exception e) {
            JOptionPane.showMessageDialog(this,
                    e.getMessage(),
                    "ERRO",
                    JOptionPane.ERROR_MESSAGE);

        }
        atualizarTabela();
    }//GEN-LAST:event_jButton4ActionPerformed

    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        if(evt.getClickCount() == 2){
           int i = jTable1.getSelectedRow();
            jTextField1.setVisible(true);
            jTextField2.setText(((DefaultTableModel) jTable1.getModel()).getValueAt(i, 0).toString());
            jTextField1.setText(((DefaultTableModel) jTable1.getModel()).getValueAt(i, 1).toString());            
            jButton4.setEnabled(true);            
            jButton3.setEnabled(false);
       
    }
    }//GEN-LAST:event_jTable1MouseClicked

